package bitcamp.java100;

public class Test11_4{
    
    public static void main(String[] args)
    {
        System.out.println(100);
        System.out.println(0144); // 100의 8진수
        System.out.println(0x64); // 100의 16진수
        System.out.println(0X64); // 100의 16진수
        System.out.println(0b01100100); // 100의 2진수
        System.out.println(0B1100100); // 앞쪽의 0 생략 가능
    }        
    

}